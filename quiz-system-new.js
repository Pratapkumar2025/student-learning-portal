// ===== Select Mixed Difficulty Questions =====
function selectMixedDifficultyQuestions(questions, count) {
    // Categorize by difficulty
    const easy = questions.filter(q => q.difficulty === 'easy');
    const medium = questions.filter(q => q.difficulty === 'medium');
    const hard = questions.filter(q => q.difficulty === 'hard');

    // Calculate distribution (40% easy, 35% medium, 25% hard)
    const easyCount = Math.ceil(count * 0.40);
    const mediumCount = Math.ceil(count * 0.35);
    const hardCount = count - easyCount - mediumCount;

    // Shuffle and select from each category
    const selectedEasy = shuffleArray(easy).slice(0, Math.min(easyCount, easy.length));
    const selectedMedium = shuffleArray(medium).slice(0, Math.min(mediumCount, medium.length));
    const selectedHard = shuffleArray(hard).slice(0, Math.min(hardCount, hard.length));

    // Combine all selected questions
    let selected = [...selectedEasy, ...selectedMedium, ...selectedHard];

    // If we don't have enough, fill with random questions
    if (selected.length < count) {
        const remaining = shuffleArray(questions.filter(q => !selected.includes(q)));
        selected = [...selected, ...remaining.slice(0, count - selected.length)];
    }

    // Mix the order (so questions go from easy â†’ medium â†’ hard gradually)
    return gradualDifficultySort(selected);
}

// ===== Sort Questions for Gradual Difficulty Increase =====
function gradualDifficultySort(questions) {
    const easy = questions.filter(q => q.difficulty === 'easy');
    const medium = questions.filter(q => q.difficulty === 'medium');
    const hard = questions.filter(q => q.difficulty === 'hard');

    // Interleave for smooth progression
    const result = [];
    const maxLen = Math.max(easy.length, medium.length, hard.length);

    for (let i = 0; i < maxLen; i++) {
        if (i < easy.length) result.push(easy[i]);
        if (i < medium.length) result.push(medium[i]);
        if (i < hard.length) result.push(hard[i]);
    }

    return result;
}

